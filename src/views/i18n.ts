const resolveLocale = (): Locale => {
  const config = vscode.workspace.getConfiguration('cortex');
  const configured = String(config.get('language', 'es')).toLowerCase();
  return configured === 'en' ? 'en' : 'es';
};

export const t = (key: keyof Messages, params?: Record<string, string | number>): string => {
  const locale = resolveLocale();
  const dictionary = STRINGS[locale];
  let value = dictionary[key] || STRINGS.es[key] || key;
  if (params) {
    for (const [paramKey, paramValue] of Object.entries(params)) {
      value = value.replace(`{${paramKey}}`, String(paramValue));
    }
  }
  return value;
};
